// Naming rules for Variables

// 1. Variables must start with a letter, _, or $
// 2. They cannot contain spaces or start with a Number
// 3.Use camelCase for naming

let firstName = "Jeremiah";
let _age = 30;
let $price = 100.41;
// The Above declarations are all valid

